    <p>&nbsp;I can still remember sitting on the floor in front of Brother Jerry during children&rsquo;s time in our small sanctuary in Parkway Village. &nbsp;That was over 50 years ago, but his lesson is one I often remember. One of my 6 year old friends asked, &quot;Why does God let bad things happen&quot;? Brother Jerry was a simple, but so effective young minister at the time. He answered with a question of his own, &ldquo;Does anyone remember one of the greatest gifts given to us by God?&rdquo; After a few puzzled seconds he replied, &quot;free will!&rdquo; Man has the ability to be good or bad. And sometimes, bad things happen to good people. He went on to tell us why it is so very important that children of God, like ourselves, should keep doing really good things each day. That we should remember each of us is a part of God, and we should treat each other as we would ourselves. 
    <p>Well there are a few lessons in there. But it is what I try to remember when so many bad things happen in the world. The activities of last week should make each of us pause and remember to treat each other as we would ourselves. We at Champion, which is my family, try to treat each other better than we would ourselves. It is a lofty goal and I fail often.  You will see below how we are doing our small part to help our community.    
<p>Which brings us to the good news! Champion is being honored as the recipient of the Leadership Memphis Community Champion Award! </p>
   <p>Champion Awards &amp; Apparel begins its 44th year of providing recognition awards and custom apparel to the Mid-South and Nationwide. From the beginning, Susan Bowen Lane shaped our company into one that would give back to its community. &quot;To whom much is given, much is expected in return&quot; she would teach.</p>
   <p>&nbsp;<br />
     When Mike and Suzie Bowen purchased the company in 2000, this philosophy was written into Champion&rsquo;s Vision statement. &quot;Our Vision is to improve the quality of life for our employees and customers&nbsp;with our quality products and services.&quot;&nbsp;&nbsp;Champion has provided special pricing and services to over 100 non - profits and their events. This amounted to over 6 figures in 2012. This mission helps non-profits and causes raise or save hundreds of thousands of dollars each year. &nbsp;Champions employees are also giving the most precious commodity of all, their time. In 2012, our 35 employees logged attendance at over 150 non-profit events! From the AutoZone Liberty Bowl, which raises funds for St Jude, to the Youth Villages 5k, to the King Biscuit Blues Festival, which raises much needed funds for the impoverished Delta. Champion just raised over $5,000.00 for &ldquo;The Fallen Officer Memorial&rdquo; by selling its &ldquo;Memphis Rocks&rdquo; tees. &nbsp;This is not only our Vision but also our Mission.<br />
     </p>
   <p>Mike serves on the Boards of Leadership Memphis, and WestCore, holds a position as President of City Commons Home Owners Assoc., participates in the Lipscomb Pitts Breakfast Club, and too many other activities to list. Suzie Bowen is President of CASA, for abused and neglected children. Colby Bowen is involved with Special Olympics and Christian Golf. Michael Bowen is currently President of his class at Tulane Law. Champion&rsquo;s VP of sales, Bill Kinkade is a major leader in Marshall County, and former President of The Chamber of Commerce. He is presently Representative of District 52 of the Mississippi House of Representatives. Susan Lane still continues her teaching as former Chairman of Score and in area prisons. She, Suzie and the Champion Management team were instrumental in helping Mike to begin hiring &ldquo;Late Bloomers&rdquo; at Champion in 2008. That&rsquo;s the name he gave ex-offenders or felons, as we know them. There are over 22,000 felons in Shelby County presently. Over 80% are out of work or back in prison within 3 years. The cost to the county and state is over $ 36,000 a year per felon. In five years Champion has trained over 100 Later Bloomers, hired nineteen fulltime and presently retains nine.<br />
     Champion is truly improving the quality life in our Community.&nbsp; </p>
