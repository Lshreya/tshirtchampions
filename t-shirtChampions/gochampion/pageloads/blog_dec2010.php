<i>Power of One (Part XI)</i><br/><br/>
<strong>The Power of Santa Claus</strong><br/><br/>
December 2010<br/><br/><br/>




I have an unabashed belief in Santa Claus. I have over 100 around the house this time of year. Wouldn’t it be great if your job were to make people happy by giving them exactly what they wanted at least once a year?
<br/><br/>
In reality, most of us have a form of that job. The famous sales teacher Zig Ziglar said, “Give enough people what they want and you will get what you want.” It is so true. Still, I constantly have to remind myself that my job is about giving. Not just during ‘Tis the season, but year ‘round. Giving of myself to my family, friends, employees, and customers is the real meaning of my life. It is what makes me the happiest.
<br/><br/>
For your enjoyment below is a reprint of the Sept. 21, 1897 “Yes, Virginia, there is a Santa Claus.” Here’s to you receiving peace love & happiness throughout the year.  M
<br/><br/>
<i>
"DEAR EDITOR: I am 8 years old.<br/>
"Some of my little friends say there is no Santa Claus.<br/>
"Papa says, 'If you see it in THE SUN it's so.'<br/>
"Please tell me the truth; is there a Santa Claus?
<br/><br/>
"VIRGINIA O'HANLON.<br/>
"115 WEST NINETY-FIFTH STREET."                                           
<br/><br/>
VIRGINIA, your little friends are wrong. They have been affected by the skepticism of a skeptical age. They do not believe except [what] they see. They think that nothing can be which is not comprehensible by their little minds. All minds, Virginia, whether they be men's or children's, are little. In this great universe of ours man is a mere insect, an ant, in his intellect, as compared with the boundless world about him, as measured by the intelligence capable of grasping the whole of truth and knowledge.
<br/><br/>
Yes, VIRGINIA, there is a Santa Claus. He exists as certainly as love and generosity and devotion exist, and you know that they abound and give to your life its highest beauty and joy. Alas! How dreary would be the world if there were no Santa Claus. It would be as dreary as if there were no VIRGINIAS. There would be no childlike faith then, no poetry, no romance to make tolerable this existence. We should have no enjoyment, except in sense and sight. The eternal light with which childhood fills the world would be extinguished.
<br/><br/>
Not believe in Santa Claus! You might as well not believe in fairies! You might get your papa to hire men to watch in all the chimneys on Christmas Eve to catch Santa Claus, but even if they did not see Santa Claus coming down, what would that prove? Nobody sees Santa Claus, but that is no sign that there is no Santa Claus. The most real things in the world are those that neither children nor men can see. Did you ever see fairies dancing on the lawn? Of course not, but that's no proof that they are not there. Nobody can conceive or imagine all the wonders there are unseen and unseeable in the world.
<br/><br/>
You may tear apart the baby's rattle and see what makes the noise inside, but there is a veil covering the unseen world which not the strongest man, nor even the united strength of all the strongest men that ever lived, could tear apart. Only faith, fancy, poetry, love, romance, can push aside that curtain and view and picture the supernal beauty and glory beyond. Is it all real? Ah, VIRGINIA, in all this world there is nothing else real and abiding.
<br/><br/>
No Santa Claus! Thank God! he lives, and he lives forever. A thousand years from now, Virginia, nay, ten times ten thousand years from now, he will continue to make glad the heart of childhood.
</i>


