      <p class="darkertext"><strong>Teamwork & Good Deeds</strong></p>
      <p>
      <br />June 2013</p>
      <div>
  <div>
<p>
Some folks will say this is not an article on business, and some folks may be right. But this past week the importance of teamwork at Champion saved the life of a dying puppy.
</p>    
<blockquote>
    Warning! This story could make you tear-up!
</blockquote>  
<p>
Our t-shirt printing factory is on the banks of John's Creek at Mendenhall and Winchester in Memphis. I swam in the creek as a wee lad. It was only fitting that in 1981 my Dad built our future business home where we literally grew up. We are still there today.
</p> 
<p>
Being on a creek for over 30 years, we have seen all kinds of wildlife literally at our back door. Cats, dogs, raccoon, squirrels, deer, fox, birds, possum, snakes, turtles, rabbits, fish, even humans can be seen on the banks of our creek. Quite often the outdoors comes indoors, whether invited or not. The other day, was one of those days, but different.
</p>
<p>
 It was late in the afternoon when a very malnourished stray dog, (a Chihuahua/Terrier mix from all appearances) entered the building from the back near the creek. She was not afraid of people and was obviously someone's lost or abandoned dog. It was hot and rainy, and the last thing I needed was to deal with another stray. We have taken in many in my life. I hurriedly picked her up and shooed her out the warehouse door, hopefully before my wife Suzie could see it! Suzie has actually made me stop in the road so she could pick up a turtle to move it to safety! By the time I got back up the front office, (a 5 minute walk) to ask Suzie when she was leaving for the day, the dog was under her desk!
</p>
<p>
Dammit'! My son Colby, (who has the heart of his mom) saw the dog out in front of the building and Sure 'Nuff picked it up. Naturally he took it to Dad and Mom. We tried to get "Little Bit" to eat and drink. The dog was so weak and malnourished that it couldn't chew. A plan was made, and Colby was to take it home and bottle feed it and try some TLC. That night, Suzie placed an alert on Facebook to our 4000 followers; sadly there were no takers. The next morning Colby was to take the dog to the Humane Society. He couldn't bring himself to take it, so back to Champion they came. It went to another softie in our office, Diane, to care for Little Bit that day.
</p>
<p>
Then a miracle happened. Kayla and Ricky who run our production, somehow saw the dog and decided that she would be perfect as a first pet for their six year old, Logan. They took the dog home and Logan fell in love! He promptly named her Princess, (which happens to be the name of Suzie's departed Sheltie.) Was this coincidence, or Karma? You choose. There's no doubt however that it was Teamwork that made this good deed come true!
</p>
<blockquote>
<strong>TEAMWORK</strong> - There are three types of team members, those who get things done, those who watch things get done, and those who wonder how so much got done.
</blockquote>
