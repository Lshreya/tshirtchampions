<i>Power of One (Part IX)</i><br/><br/>
<strong>Power of One-One Team</strong><br/><br/>
October 2010<br/><br/><br/>





Teamwork makes the dreamwork! There is no I in team. Teamwork is not everything it’s the only thing. Team means family. The Power of One Team is here!
<br/><br/>
<strong>T</strong>ogether<br/>
<strong>E</strong>veryone<br/>
<strong>A</strong>chieves<br/>
<strong>M</strong>ore
<br/><br/>
I have used these sayings for most of my life. My very existence as a child was based on which team I was on at a particular time. I have since discovered that you must have a healthy sense of individuality to be a really productive member of a team.
<br/><br/>
As many of you know Champion and it’s numerous companies has been family owned and operated for over 40 years now. On one hand this has allowed us to be consistently successful. On the other, it has been difficult to build a successful team outside the parameters of the “Bowen Dictatorship.”
<br/><br/>
Several years ago when my wife and I decided to buy out my mother and brother it was with great trepidation! Now, we were going to ultimately depend on a team of non-family members for our future success.
<br/><br/>
I began putting together a management team of our department managers and having regular meetings. I tried to teach them everything from employee relations to reviewing profit and lose statements. I languished at this process. I had the wrong players. We were losing customers and profitability and I basically just bitched more. What was I missing? Well actual teamwork was missing. I had failed to actually form our team financial goals with a mission, core values, a purpose and the empowerment to accomplish true teamwork. So what was I going to do next?  A corporate psychologist once told me, “for a company to make the transition from dictatorship to a team based company, the dictator has to get out the way.”
<br/><br/>
Four years ago I had to take some time off for family reasons.  My “ new team members” stepped up beyond expectations. Three years ago I had to take some more time off for a surgery. Again the team worked better together than ever before. Mind you, during this time we had begun regular departmental meetings without me. We really made our mission and purpose focal points of exactly why we were doing what we do.
<br/><br/>
<strong>Mission:</strong> Our Mission is for all customers to be completely happy with our products and service.
<br/><br/>
<strong>Purpose:</strong> To be the absolute best quality source for custom branded awards and apparel to the best people in the world… Our Customers.
<br/><br/>
Bad news, two years ago we had our first wage freeze in the history of the company. Good news, we based future compensation on specific company profits.  Everyone responded tremendously. We raised our profit goals based on a daily Win/Loss Report of new customers, sales and actual on time orders shipped. We have since added core values to our team as well.
<br/><br/>
<strong>Core Values:</strong> Trust, Quality, Loyalty, Integrity, Empowerment, Ownership
<br/><br/>
The response has been absolutely phenomenal! We are still far from where we want to be. But Champion Awards and Apparel is truly based on teamwork. Now our team has become the "Champion Family."
<br/><br/>
“Individual commitment to a group effort - that is what makes a team work, a company work, a society work, a civilization work.” -Vince Lombardi
<br/><br/>
Champion is having one of its best years ever. All I had to do was set attainable goals, empower knowledgeable good people, continued training and communication. Also, I got the heck out of the way. It only took me 40 years to figure this out. I hope this article will save many of you a little time in your effort to obtain the “Power of One Team.”

