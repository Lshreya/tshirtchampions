      <h2><strong>Hands of Hope 
</strong><br /></h2>
      <p>February 2015</p>

      <div>

<p>This week’s blog will be from Suzie Bowen who is on the board of The Exchange Club Family Center. </p>


<p> “Child abuse and neglect offend the basic values of our state. We have a responsibility to provide safe settings for at-risk children and facilitate permanent placement for children who cannot return home.”- Matt Blunt</p>
 
<p><a href="http://www.exchangeclub.net/hhtickets" target="_blank"><img src="images/handofhope.png" width="490" height="300" alt="The Exchange Club of Memphis"/></a></p>
 
<p>The Exchange Club Family Center is celebrating their 30th year working to help victims of domestic violence and child abuse. Offering 25 programs, The Exchange Club Family Center works with victims to overcome the effects of violence and abuse. These programs help the victims on an emotional level as well as teaching them necessary life skills to prevent the cycle of abuse to reach the next generation.  It is a known fact that abused children often become the abusers themselves.  </p>
 
<p>The Exchange Club Family Center works with victims as young as 2 years old. They do this by offering "Play Therapy" geared toward children who often are not able to vocally express their emotions and fears. The center also works with at risk teen mothers in a program called "First S.T.E.P.S."  This program teaches these young mothers prenatal care, parenting skills, job readiness, and anger management. The "Respect" program specifically addresses bullying by teaching children how to handle their emotions constructively, be assertive without being aggressive, and to develop healthy friendships. The center works with parents with such programs as parent-child interaction therapy, counseling, and parent education classes. Furthermore, the center has several programs geared toward divorcing, separated, and non-married parents. "Transparenting" is a four-hour seminar that teaches effective parenting making these life transitions easier on the child. The center also does supervised visitation, therapeutic visitation, where a "coach" is with the parent helping them restore a broken relationship, and a safe exchange program. Children can be safely transferred from one parent to the other because the center is a safe exchange site.</p>

<p>In 2013 the Exchange Club Family Center brought CASA (Court Appointed Special Advocates) in to their fold. CASA is an agency that trains volunteers from the community to investigate child and abuse cases for Juvenile Court. While CASA has been a vital resource to Juvenile Court for 28 years, a loss in funding threatened to close CASA's doors. Through the efforts of executive director, Barbara King, and the Exchange club board as well as the CASA board, an agreement was made for the Exchange Club to take over CASA. This has resulted in continuing this program that is vital to Juvenile Court. Dan Michael, our newly elected juvenile court judge, was the first executive director of CASA. The organization is now poised to thrive and grow thanks to the efforts of the Exchange Club Family Center.</p>

<p>Roughly 25% of reported child abuse cases in Tennessee are in Shelby County. The center has worked diligently over the last 28 years to reduce that number. Early intervention is vital to keep these victims from repeating the same cycle of abuse.</p>

<p>The impact this center has made on our community is more than just facts and numbers, it is also in the faces of past clients who become advocates for the center and dedicate their lives to continuing the good and vital work of the center. 
</p>

<p>The Exchange Club Family Center is having their annual Hands of Hope Auction Party. Tickets are $100 per person. Please click on the link at the top to obtain tickets to this fun event. There will be music by Magi and CharveyMac. If you can donate to our auction, please contact me at 901-921-8362 or email me at suzbow@comcast.net. No item is too large or small. The funds from this event will go towards continuing our work of helping victims overcome the effects domestic violence and abuse and go on to build new lives with bright futures. Many times these folks come back to the center and tell us they too have dedicated their lives to helping others that have been down the same road they have and so the centers work truly goes on.</p>

 
<p>Thank you,<br />
Suzie Bowen </p>
