  <p class="darkertext"><strong>Power of the T-Shirt</strong><br />
    <br />
    October 2011<br />
  </p>
  <p> I’m finally getting rid of this writers block I’ve had for a couple of months now. It’s been a trying period for our family with personal losses.  With all the hard stuff going on in the world today, I sometimes forget that we not only have a loving group of people around us but that our business does bring a little joy to so many people. I remembered this again recently at the King Biscuit Blues Festival. We have designed a line of t-shirts for resell for the King Biscuit Blues Festival in Helena, Arkansas for over 16 years. About 7 years ago, they asked us to throw t-shirts from the stage between acts and promote the festival store. We do this between acts and we have become famous, (in our minds for three days.) Nevertheless, we have a ton of fun and forget our worries for three whole days. Truly the Power of the T-Shirt!</p>
  <p> So, this whole time, right in front of me, was the Power of the T-Shirt. Champion Awards and Apparel prints over 50 orders of t-shirts a day, from six pieces to twenty-five thousand pieces. Our little family company of 25 employees has printed over one hundred million tees since 1977, WOW! But the most important order is the last one we finished no matter how big or how small. T-shirts serve all kinds of purposes in our world today.</p>
  <p> You know, there’s got to be millions of types of t-shirts but I’m going to break it down into only five categories. There are Attitude T-Shirts, Art T-Shirts, Advertising T-Shirts, Sports T-Shirts and my favorite, Cause/Family Reunion T-Shirts. <br />
  </p>
  <p>T-shirts with an attitude often called Slam Shirts show off person’s inner beliefs on their chest. A few of the most popular are below:</p>
  <p>“A positive attitude may not solve all your problems… but it may annoy enough people to make it worth the effort.”</p>
  <p>“How old would you be if you didn’t know how old you are?”</p>
  <p>“Its better to be hated for what you are, than to be loved for something you are not.”</p>
  <p>These t-shirts are all around us and empower the wearer to make a stand in a passive aggressive manner.<br />
  </p>
  <p> Art or Designer T-shirts can cover the gamut too. We at Champion feel we are one of the best in the country at staying true to fine art pieces from Museums to Festivals. The t-shirts below truly allow the wearer to have what I call the “working man’s art” in their everyday life.  Just Great Stuff!</p>
  <p><img src="images/banner2.jpg" width="490" height="122" alt="Tees" /></p>
  <p> Advertising, Corporate or Business T-shirts are our bread and butter. Next to a business card, the t-shirt is the most printed commodity for small to big business. If your business doesn’t offer or supply t-shirts to your employees or customers you’re just not official.  We have over 2000 customers (always need more) and over half are small businesses using tees for advertising and uniforms.<br />
    <img src="images/adlogos.jpg" width="488" height="46" alt="adlogos" /></p>
  <p> Sport T-shirts or Team Tees are probably the most popular. We have printed t-shirts for every major sporting event in the top four sports for over 30 years! We just completed 12,000 for the St. Louis Cardinals National League Championship on Sunday night. They were in the stores in St. Louis by 10am the next morning. Yes, we are Cards fans. We also have been lucky to be the longest local licensee for University of Memphis for over twenty years. You know we’re hoping they both go win their last games!</p>
  <p><img src="images/nlcs.jpg" width="324" height="131" alt="nlcs" /></p>
  <p> My favorite T-shirt is the “Cause” T-shirt. It just makes you feel good! Since October is Breast Cancer Awareness month here are two of the many special tee’s we will do for this great cause. </p>
