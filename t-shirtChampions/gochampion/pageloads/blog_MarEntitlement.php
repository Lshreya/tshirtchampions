      <h2><strong>Take Action Now 
</strong><br /></h2>
      <p>March 2015</p>

      <div>

<p>"Entitlement is the opposite of Enchantment ". Guy Kawasaki, author and entrepreneur.</p>
 
<div align="center"><img src="http://gochampion.net/images/Tultex.jpg" width="500" height="350" alt="Call for our Valentine's specials"/>
  
</div>
<p>I was 5 years old. I had just learned how to ride a bike without training wheels. I "got" to deliver clothes my mom had just sewn for the neighbors. It was such a big deal because I also put a basket on my bike to hold the AVON she sold. I made a nickel per delivery. I say I “got” to because I was so enchanted that she trusted me to deliver her work that I would have done it for free. To this very day, I try hard to remember I "get" to serve people instead of "have" to.</p>
 
<p>My parents never entitled me to material possessions. We received "hours and chores" instead of allowance. I was taught that I am entitled to work hard and improve everyday. That is the main thought I still carry today. As I have grown older, it has become much more part of me than I ever knew. I am not good with people who feel entitled. I have to be very careful in my reactions to my children, employees, and customers who I think feel entitled.</p>

<p>My son Colby is learning to become our sales manager. He bears the brunt of these emotions from time to time. I am constantly wishing I had a "do over" in our conversations. Also his cousin, Brandon our VP of Operations, catches his fair share of my passion and sometimes vehemence. Anyone who has tried to manage relatives in a business understands this dilemma.</p>

<p>Successfully walking the tight rope between entitlement and the "passing of the torch " can be frightening. I certainly don't want to be that President who wouldn't let go. However, I also want my children to earn the right to lead others. To accomplish this feat they must be smarter, quicker, and work harder than all those around them. Most importantly they must do it without a sense of entitlement. They must stay enchanted with "the journey of improvement."</p>

<p>To serve and improve others around you is the ultimate goal.</p>

<p>Stay enchanted.</p>
<p>Love,<br>
 
  <img src="http://gochampion.net/images/mikesignature.jpg" width="200" height="42" alt=" " title="MikeSignature.jpg" /><br>
Mike Bowen</p>

