  <div>
    <div>
      <p>      One word, JOBS. This is the major solution in keeping people out of prisons. We lost Cal. He just disappeared after his required stay at the halfway house. Champion had cut his hours. He must have thought he couldn't make it on less than 30 hours week. We, like other small businesses have busy seasons when we need extra employees. Cal, Marcus, Terrance and Darrius, are just a few of the excellent temporary employees at Champion Awards and Apparel, who have had their hours reduced. It has nothing to do with their performance; it is simply the nature of our business that requires a smaller staff during the off- season.</p>
      <p> If you read either of my previous two articles, you would know these men and women are just a few of the 25,000 felons on probation or parole in Shelby, Desoto, Marshall, Fayette, Tipton, and Crittenden counties. They are struggling to re-enter the workforce after their release from prison.</p>
      <p> Here are the numbers in all their sad glory: (From <a href="http://www.westcore.org" target="_blank">WesTCORE</a>)</p>
      <p>•22,606 - local 6 county prison population in 2011. Includes Federal, State, County, and City facilities.<br />
        •60,000 plus felons on probation or parole in 2011, in the State of Tennessee alone.<br />
        •Over 50 % are back in prison within 3 years.<br />
      </p>
      <p>Part of the major reason is felons cannot survive (eat) without committing crimes is because no one will hire them! I don't know about you but I 'm tired of paying for this broken system.</p>
      <p>The solution: Hire a Felon! </p>
      <p> A coordinated reentry strategy that is community based, focuses on securing stable housing, developing jobs, providing support, and counseling for participants and their families has shown to reduce recidivism by 30%.</p>
      <p> A 30% decrease in new felony admissions represents a one-year savings of $46 million to the Department of Corrections. Additional savings will go to local law enforcement, probation and parole boards, and the judicial system at all levels. I don't know about you, but I would rather spend that $46 million on something that works. The West Tennessee Coalition on Reentry works. WesTCORE is a group of halfway homes, counseling services, and job training organizations that are committed to creating The Solution.</p>
      <p>&quot;The dictionary is the only place where Success comes before Work. Hard work is the price we must pay for success. I think you can accomplish anything if you're willing to pay the price.&quot; - Vince Lombardi</p>
      <p> So lets find businesses, churches, and organizations that will hire just one felon each. There are over 5000 members of Chambers of Commerce in the Memphis MSA. There are over 2000 places of worship in the same area. That is over 7000 felons we could give a second chance. I think we can find at least 700 (only 10%) a job, any job.  I am challenging this community to step up!! </p>
      <p> I so appreciate this pulpit you have given me to discuss some of the ills of our community. I just believe that the only thing most people want is an honest way to feed and clothe themselves and their families with a little dignity. If they don't have that chance, these people are left with few alternatives.</p>
      <p> Be a part of the solution! Contact <a href="http://www.westcore.org" target="_blank">www.westcore.org</a> with a job, any job today!  It will make you feel good and this community a better place to live.</p>
