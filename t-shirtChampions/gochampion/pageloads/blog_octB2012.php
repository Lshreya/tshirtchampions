      <p _mce_style="padding-left: 30px;">Those of you who read my blog on a regular basis must know I abstain  from writing about religion or politics. This blog is no different,  except my friend is stepping up in an attempt to serve us in a  governmental capacity. I believe this is one of the bravest acts a man  or woman can do for us.<br />
        <br />
              Bill Kinkade, my friend, my boys&rsquo;  second dad, the VP of Sales for my company and now my State  Representative. I say my State Representative even though I live in  Memphis. Bill is running for office in District 52 (Marshall and Desoto  Counties) in the great state of Mississippi. What happens in Mississippi  often affects Memphis, Tennessee. That is why he is my State  Representative to be also.<br />
        <br />
            Now to the crazy part!! Can you  believe a fifty-five year old man with his home paid for, his children  grown with nice jobs and homes, his grandchildren crawling on his knees  each weekend, finding time to go hunting, and with his job secure as  long as he wants to work, would run for political office? If you know  Bill, you will understand.<br />
            <br />
        &ldquo;To whom much is given, much is  expected.&rdquo; This is a simplification of a quote from Luke in the  Christian Bible. FDR, John Kennedy, and Ronald Reagan all used this as a  platform for their service to mankind. Bill is no different; he has  felt the need to serve the community since I met him on my plant floor,  printing t-shirts, 26 years ago.<br />
            <br />
             Bill did not have much  then, a broken down van and living with in-laws in Coontown, Mississippi  on Sardis Lake. He came from even less. Because of financial hardship  in 1962, Bill&rsquo;s mother was forced to place him (at age five) in the  Methodist Children&rsquo;s Home  (a fancy name for orphanage) in Jackson,  Mississippi. He spent his formative years there until he was twelve.  This could have caused a deep scar for many of us. In the 60&rsquo;s  orphanages were akin to child prison camps. Bill will tell you he was  one of the lucky ones.<br />
        <br />
            In 1969, his grandparents from  California rescued him and took him to live with them in Orange County  where he received a good education. After high school, Bill joined the  Army where he took college level classes. Afterwards, he married and had  two beautiful children and moved back to Mississippi to be near his  wife&rsquo;s aging parents. He also wanted to raise his children in  Mississippi. He worked hard at Champion, bought a piece of land in  Byhalia, raised his children, and became a leader in the community. He  helped form the Byhalia Sports Association, served twice as President of  the Chamber of Commerce and he is now Zoning Commissioner. The man has  it made. Why run for political office when you have it made? Bill  Kinkade doesn&rsquo;t need another job, but he will tell you it is in his DNA  to serve. I tell you it is his bravery and destiny. <br />
        <br />
             This  race is not about political party; it is about the citizens of Desoto  and Marshall Counties having a person who will listen to them and  represent them in Jackson. Vote for BILL KINKADE, a brave man; I would  if I could. Please go to www.billkinkade.com top help Bill in his run to  represent citizens of Mississippi.</p>
