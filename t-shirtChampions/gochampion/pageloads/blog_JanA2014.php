      <h2><strong>A New Era for Champion Awards and Apparel</strong></h2>
      <p>January 2014</p>
      <div>
<p>Dear family, friends and valued customers,</p>
<p>It is with much excitement that I share with you the next evolution of Champion Awards and Apparel, a third-generation family-owned company since 1970.</p>
<p>But let me go back to almost 18 years ago when this thing called the Internet became a necessary tool of business. Believe it or not, we had an e-store for Fred Smith's (yes, that Fred Smith’s) Canadian football team, the Memphis Mad Dogs. In 1996, we sold a grand total of $339 in Mad Dogs merchandise on the Internet. It was all sold to Mr. Smith and his friends. We had ChampionAwards.com for a couple of years until we realized that no one really cared about online retail (e-commerce) yet. So, in my infinite wisdom, I gave up the domain. In 2001, we bought gochampion.net and memphisstuff.com.</p>
<p>We have since bought more than 100 domain names looking for one that "searched” - meaning it was easy to find on the Internet.</p>
<p>The main reason for this Internet push was to repair the biggest mistake I ever made in business. That was to merge my mom's Champion Trophy and dad's Custom Printed Tees companies together under the professional name of Champion Awards and Apparel in 1986. This was a big mistake because by 1998, we knew that our industry was ripe for Internet marketing. And we were right. Believe it or not, the words “trophy” and “tees” are what people use for searches. “Awards” and “apparel” just aren’t. To make matters worse, it has been very difficult to explain in a sentence or two all the services that our diversified company provides. It has become my albatross. We have simply needed to define our core business for a long time. As the leader, it has seemed like forever.</p>
<p>Champion is the oldest and best T-shirt printer in the Mid –South, hiring local team members and giving many people an opportunity when others wouldn’t. We focus on small businesses, charities, schools, churches and events. I’d say we are the best T-shirt printer in the country! Come take a tour and see for yourself. And, I am happy to announce we are still going to improve even more!</p>
<p>Soon, I will have my albatross lifted because our company direction will simply be: T-Shirt Champions. We make T-shirts easy! We will launch this platform with a huge party Friday, February 28th and Saturday, March 1st. (So, save the date!) More on this huge blowout will come later.</p>
<p>Our major goal with this decision was to make it easy to design and order T-shirts and to make it easier for our team to serve our customers. We have built a business plan that focused on five main improvements.</p>
</p>First, we built a team. I promoted Brandon Conners to vice president of customer care and put together a brilliant young team of service oriented designers. They are now called the Champions, and Brandon is the head coach. They have laid the foundation to define our core business for our loyal customers. More importantly, we have streamlined our services via the web for the whole Mid- South and a new generation.  </p>
<p>Secondly, we made ordering T-shirts easy. We built a business plan for the future of T-Shirt Champions that broadens its reach and makes it easy for you, the customer, to order T-shirts with no rules. Up front wholesale prices, free design, free set-up and free shipping, all delivered in 10 days or less.</p>
<p>Thirdly, we have made T-shirts fun! We have a free graphics library of more than 10,000 designs that makes it fun to create your own T-shirt design. Thanks to Nikki Fierson, our new art director, the library will continue to grow. Also, with games, prizes and loyalty discounts, we will develop a social community of T-Shirt Champions. Our Facebook page has more than 1,000 fans, and after this formal launch, we expect even more.</p>
<p>In the plan, we also created the concept of “T-Shirts Give Back.” We have more than 300 nonprofit customers, and we serve for the good of the public. We have been involved with improving the quality of life in our community for more than 43 years. We wanted to step up these efforts up by donating a minimum of 10 percent of our proceeds back to the Mid-South. Our customers will now be able to participate with our manager of communications, Marissa Croft, who promotes our good deeds.</p>
<p>Finally, we have always believed in “The Champion Way.” That way is "we’ve got your back." Simply put, we will make you happy. We guarantee 100 percent happiness, or we will redo your order. I guarantee it!  TShirtChampions.com will be our new platform for all the good people out there who want to design and order their own T-shirts online. Champion's famous customer care that you have known since 1970 will not change. We will still operate business as usual for all those folks who, like me, want and need help designing that special sportswear item!</p>
<p>But, our web design tool will also be so easy that it may surprise even the most design-challenged!</p>
<p>Why? Because we have put together a team of champion designers that understand customer care. And they literally read minds! I swear! I am so confident in our service that the first 100 test customers will receive 12 free T-shirts with any order! Also, for a limited time, any order over 100 t-shirts will be only $5 per shirt.</p>
<p>Thanks for this opportunity! Together we can do this! </p>


