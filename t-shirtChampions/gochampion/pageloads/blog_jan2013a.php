<p class="darkertext"><strong>Fatigue Makes Cowards Of Us All</strong><br />
  <br />
December 2012</p>
<p class="darkertext">&nbsp;</p>

<div>
  <div>
    <p>The year has just begun, and I am already tired.<br />
      Two Thanksgiving dinners, two bouts with flu, 22 employees with flu over the last month, last minute crazy service to customers for Christmas, three Christmas dinners prepared, AutoZone Liberty Bowl activities (10 stores built within 4 days), Godaddybowl.com activities (6 stores built within 3 days), National Championship printing for Nike all night! Should I go on?</p>
    <p>Usually, I don't get tired. I watch fatigue, because I'm not fun to be around when I'm tired. There's a joke there, but I'll leave it be. The point here is, if I'm tired I know how really tired my team members at our small company should be. And they are too! But in the scheme of things, who really cares?<br />
        <br />
      &quot;Sentimentality is a form of fatigue&quot; - Leonara Carrington, Famous Mexican Artist.   <br />
      <br />
      Well, I care!  As a small businessman, the most important component of my business is my Team.<br />
      <br />
      &quot;People, Service, Profit&quot;. This slogan by Fred Smith of FedEx fame is my mantra. But, I am hard on my team.<br />
       <br />
      &quot;I ask people to do the things they don't want to do to become the people they always want to become.&quot; - Coach Tom Landry.<br />
        <br />
      Yes, I'm tired, but you can rest assured my team is tired too.  And the year has just begun! As a more dynamic but compassionate leader, I am still learning how to pull the feelings out of my team.</p>
    <p>First, I have to take the time. Second, I must listen. Third, I must stay rested.  Most of all, it's my job to not let the fatigue in my team force them to become cowards in 2013. So, when you read this, I will be in Destin. Walk on beach, eat, sleep, and repeat. Stay rested and healthy. The year has just begun!</p>
