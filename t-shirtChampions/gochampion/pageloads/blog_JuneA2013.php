      <p class="darkertext"><strong>Resignation</strong></p>
      <p>
      <br />June 2013</p>
      <div>
  <div>
<p>I have noticed a trend in the last year or two. Nearly every week I receive at least one resume from someone needing a job. Now this fact is not unusual. What is unusual is that most of the job seekers are between the ages of forty-five and sixty. Some, after twenty-five to thirty-five years of work, have retired. Some have been bought out. Some have been laid-off or fired. A few have even quit! What they all share in common is of course the need for a J-O-B.</p>
    <p>With a little research, I discovered that out of 320 million folks in the
US, there are over 80 million of us between the ages of forty-five and sixty. That¹s over twenty-five percent of the population! It's obvious that this age group must work, not only for their own welfare, but also for the welfare of our  country.
</p>
    <blockquote>"The dogmas of the quiet past are inadequate to the stormy present. The occasion is piled high with difficulty, and we must rise with the occasion. As our case is new, so we must think and act anew. We must disenthrall ourselves and then we will save our country."
- Abraham Lincoln to Congress 1862
</blockquote>  
    <p>Now, I don't claim that our abyss of ageism is anywhere similar to the
Civil War. But we all must work! If you don't need the money, then just for your very sanity. I always suggest to someone who is out of work, especially after unemployment benefits are gone to take a job, any job. I always say, "as long as Bermuda grass and weeds are growing in Memphis, I can earn some money."  You can't always find a J-O-B in your "field", (What the hell is that anyway?) If there are no jobs in your "field", move over to the next field and keep moving. Do like my friend Chris Thomas has done. 
</p>
    <p>Chris is a big wig! He is a Shelby County Commissioner with over 25 years of management experience. He recently found himself in the job market again, but even with such an impressive resume, he hasn't been able to find the right fit in over six months. He called and asked for a job. I definitely don't hire friends, but I told him our gift shop on Beale Street needed an extra hourly clerk for May thru July. It didn't pay much. He said it fit perfectly with his other part-time job that, "didn¹t pay much". Simply put, he is working everyday of the week. Since then, he has received more job offers. Without the desperation that¹s often bred from unemployment; he now has both the time and the peace of mind to consider an offer more thoroughly. (I would hire him in a minute if he weren't a friend). Contact him at christhomas1@bellsouth.net.
</p>
    <p>The moral of this tale is three little letters, J-O-B. It can mean the difference between living and irrelevance. I choose relevance!  Do not let age and your "field" get in the way of being fulfilled. Don't resign without a J-O-B. Take a job today even if it is volunteer work. I promise good things will happen!</p>
        <blockquote>"It is the working man who is the happy man. It is the idle man who is the miserable man." - Ben Franklin
</blockquote>
