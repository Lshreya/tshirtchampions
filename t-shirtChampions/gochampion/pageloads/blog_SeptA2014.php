      <h2><strong>Soulsville Changes Lives</strong><br /></h2>
      <p>September 2014</p>

      <div>
<p><i>Guest Post by Tim Samson</i></p>
<p>On a particularly freezing cold day in January 1998, I was summoned to meet with three people at the corner of College Street and McLemore Avenue in South Memphis just on the border of Midtown. It was like a warzone. Cracked concrete, broken glass, abandoned buildings with no windows, crack houses, and garbage. I will never forget the amount of garbage that seemed to be everywhere. But on the sidewalk, near a burned out school bus, there was a historic marker alerting visitors to this site that it had once been the home to Stax Records – that on the site the careers of such artists as Otis Redding, Isaac Hayes, Booker T. & the MGs, Sam & Dave, and numerous others were launched.</p>
<p>Today, thanks to the boldness and leadership of those who had a vision far beyond putting just a tourist attraction there (and even that seemed insurmountable to many), the Soulsville Foundation’s campus is a $40 million development in one of the most economically depressed zip codes in the United States. In addition to the Stax Museum of American Soul Music (which had its busiest month since inception in 2003 this past July with 8,200 paid visitors, the campus includes the Stax Music Academy and The Soulsville Charter School. We work with approximately 700 middle and high school students every day in the two educational programs. Stax Music Academy students, most of whom live below the poverty line and attend through scholarships we provide, have performed in Italy, Australia, Berlin, Lincoln Center, Kennedy Center, and other locales. Since 2008, every senior enrolled in the academy has been accepted to college. </p>
<p>The Soulsville Charter School is a unique, academically rigorous, musically rich college prep school manned by staff and faculty who are so dedicated to helping children that it is much more of a calling for them than just a job. The school has had three graduating classes so far. All 188 seniors graduated and were accepted to college with collectively more than $20 million in scholarships and grants. </p>
<p>So when I arrive at work each morning, I am in absolute awe of the physical changes that have taken place at the former site of Stax Records since that day in January 1998. But more importantly, I think about the lives we are changing, and those that we are saving. Not many museums can boast that claim. </p>

<p><i>Tim Samson is the Director of Communications at the Soulsville Foundation in Memphis, TN. </i></p>


