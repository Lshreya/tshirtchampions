  <p class="darkertext"><strong>Life is a struggle, but we still LIVE.</strong><br />
    <br />
December 2011</p>
<p><span data-mce-style="font-size: small;">We've  all heard the saying &quot; Life is a B... then you die! Well, I&rsquo;m here to  say, &quot;that is just plain wrong.&quot; Yes, life can be tough, real tough. Let  me say that again, &quot;real tough&rdquo;.</span></p>
  <p><span data-mce-style="font-size: small;">      Last Saturday was one of those tough days. I found out a friend I've  known since childhood died doing yard work in his front yard. I was  doing yard work at the same time and he lived not more than 2 miles from  me! This weekend&rsquo;s reality got me to thinking that yes, bad things do  happen to good people. </span></p>
  <p><span data-mce-style="font-size: small;">      I don't dwell on these struggles! But I did sit down and take a short  inventory of &quot;struggles &quot; that have occurred in the last couple of  months to people I know and love.</span></p>
  <p><span data-mce-style="font-size: small;">• She spent 4 days in hospital on morphine with an intestinal situation (painful, painful situation).</span></p>
  <p><span data-mce-style="font-size: small;">• She lost her sister to cancer in one month and her best friend to cancer the next month. </span></p>
  <p><span data-mce-style="font-size: small;">• His mother almost died of COPD.</span></p>
  <p><span data-mce-style="font-size: small;">• One of her brothers had a car accident, broke his back. </span></p>
  <p><span data-mce-style="font-size: small;">• Same girl, different brother had car accident, shattered his leg.</span></p>
  <p><span data-mce-style="font-size: small;">• She discovered her husband is on his last trial of Chemo. If it doesn&rsquo;t work, that's it! </span></p>
  <p><span data-mce-style="font-size: small;">• She finally had to send her husband to nursing home. Almost worse than losing him altogether!</span></p>
  <p><span data-mce-style="font-size: small;">• He had to spend 2 full days testifying in Federal Court trial as a witness. </span></p>
  <p><span data-mce-style="font-size: small;">• She discovered her Breast cancer returned! </span></p>
  <p><span data-mce-style="font-size: small;">• He had a hip replacement. </span></p>
  <p><span data-mce-style="font-size: small;">• She had shoulder surgery! </span></p>
  <p><span data-mce-style="font-size: small;">• Discovered his wife had a brain tumor. </span></p>
  <p><span data-mce-style="font-size: small;">• A nephew of someone close died of drug overdose. </span></p>
  <p><span data-mce-style="font-size: small;">• He got a divorce from his third wife! </span></p>
  <p><span data-mce-style="font-size: small;">• He doesn't have enough money to pay tickets to get his license back to drive. So he can go to work! </span></p>
  <p><span data-mce-style="font-size: small;"> • He lost his job of 15 years in the only thing he knows how to do. </span></p>
  <p><span data-mce-style="font-size: small;"> • His wife lost hers too! </span></p>
  <p><span data-mce-style="font-size: small;">      These situations are not isolated to me. We go thru them all the time.  We will continue to go thru them. The common denominator here is health  and security. We are not guaranteed health and security. Most of us do  struggle to keep and nourish them. You've heard the saying &quot;don't sweat  the small stuff.&quot; Well as long as we have our health and can eat,  everything else is the small stuff!  It's called life and the only thing  we can control is attitude. </span></p>
  <p><span data-mce-style="font-size: small;">      I believe my generation has not spent enough effort teaching young  people that there are no guarantees in this life. Life is a struggle.  But, it is in the struggle that we can celebrate!   </span></p>
  <p><span data-mce-style="font-size: small;">There's an old saying I use to teach to young people about giving 110 % everyday,</span></p>
  <p><span data-mce-style="font-size: small;">What makes up giving 110% to life? </span></p>
  <p><span data-mce-style="font-size: small;">96% is showing up engaged! </span></p>
  <p><span data-mce-style="font-size: small;">98% is Picking something and becoming good at it!</span></p>
  <p><span data-mce-style="font-size: small;">100% is continued learning, not education, learning! </span></p>
  <p><span data-mce-style="font-size: small;">110% is a grateful and happy attitude!</span></p>
  <p><span data-mce-style="font-size: small;">We  really can&rsquo;t change anything but ourselves! So if we are lucky, we  learn from our struggles and allow them to make us stronger. </span><span data-mce-style="font-size: small;">Live life with a grateful and happy attitude!</span></p>
