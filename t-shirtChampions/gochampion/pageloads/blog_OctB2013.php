      <h2><strong>Healthcare is Hard</strong></h2>
      <p>October 2013</p>
      <div>
<p>We got back from spending ten glorious days with the whole family on the ocean in the great state of Maine. I had just forgotten about work when I went to the grocery. In the store, I overheard a discussion from two guys considerably older than me.</p>
<p>It went something like this…</p>
<blockquote> 
	<p>Plaid shirt guy: "Man this ObamaCare is scary."</p>
	<p>Man holding bag of fruit: "I don’t think it's gonna be any scarier than what we go thru now.”</p>
	<p>Plaid shirt guy: “Well wouldn't it be nice if we all just died before we got sick like we did before we started taking care of ourselves?”</p>
	<p>Man holding fruit: "You are scared aren't you?" </p>
</blockquote>
<p>Yes, people are scared. But from my 44 years of business experience (yes, my social security payments began when I was 12), people have always been scared of the doctor, hospitals, insurance companies, and the overall concept of illness. You don't need me to tell you this stuff. It's so pervasive in our society right now that we are frozen on any action.</p>
<p>My biggest worry, as an employer is the welfare of my team. If they are hurt, then I am hurt. If their children or family are hurt, then they are hurt. And again, if they are hurt, I am hurt. We have a fairly healthy team. Yes, we have the cases of Diabetes, the allergies that crop up, the high blood pressure, colds, flu, pulled muscles, and stomach maladies. But for the most part, we have a healthy team for now. But we ain't getting any younger! </p>
<p>A lot of issues have been, well, mine. In the last ten years I’ve had two heart catheters, one stint, two angioplasty procedures, a broken back, two shoulder surgeries, one sleep study, five flues, lots of colds, and, of course, allergies. </p>
<p>So, I have become the doctor of my building. I am constantly telling folks to get physicals regularly. Exercise. Visit the doctor as soon as illness arises. For the last 15 years we have enforced the policy that no one – and that means no one – works injured or sick. It works most of the time, but folks need their hours. It is really a catch 22! </p>
<p>But, how do you have a regular doctor if you cant afford co-pay? Champion has always provided half payment of major medical, but even then it is not affordable to everyone. Suzie and I decided  that by 2015 we are trying to get every team member to sign up for Insurance. That will mean our starting wages must increase. We even began paying sick days again this year and are paying for flu shots. We promote healthy eating habits. We aren't doing enough and we are going to do more. So Dr. Mike I will continue to be!</p>
<p>Or of course we can always hope people will die early before we get sick. </p>
<p>I, for one, am not that scared! We can do this!! We have always found a way and will continue the fight!! After all, we are Champions!</p>
<p>"Your living is determined by not so much as what life brings to you ; as by attitude you bring to life."<br />&nbsp;-author Kahlil Gibran</p>
