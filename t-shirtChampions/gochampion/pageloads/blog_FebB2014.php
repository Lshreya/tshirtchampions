      <h2><strong>We Make It Easy To Do Business</strong><br /></h2>
      <p>February 2014</p>
      <div>
      <p>
 We make it EASY to do business with us! How many times have we heard this? We hear it everyday.  I am asking this question while I await service for a product. A product I have delivered daily. I will let you figure out what that might be. I have waited 20 minutes (and counting) on a service person. I emailed them yesterday (Sunday) and still did not receive service today. I can't tell you how much I absolutely despise this deception. As my granddad would say, "You either tell the truth or you lie." There is no in between. (Now my wait is going on 23 minutes…I type slow!)</p>
<p>The above situation happens millions of times across the world. Millions upon millions of dollars in time waisted for lack of service. But don't dare interrupt payment. There will probably be no notice that you may have forgotten. However, it will lead to an immediate termination in said service. (Now my wait is 28 minutes.)</p>
<p>This is the main reason I wanted a web design tool for my 44 year old family company. I don't like to wait! Like many of you, I've gotten pretty good at using my little iPad, laptop or PC to order any service I need without a "customer service" representative.</p>
<p>Heck, my wife has decorated entire houses off the internet. Not to mention, a whole rotating wardrobe. We believe in this service so much we created a Web based T-shirt design platform for everyone. (After 36 minutes of waiting, the line went dead…I called back!)</p>
<p>So the the future is here. <strong>Champion Awards and Apparel is now T-Shirt Champions!</strong></p>
<p>We Make T-Shirts Easy. What does that mean exactly? It means you can design a T-shirt in less than 5 minutes. You will receive an instant verification of your order. You will receive an email or a call the same day or next day if we have any questions. You will pay one price per tee. No other charges. Nada. Nothing. Zero! One little price per shirt. And you will receive your custom designed T-shirts in seven days or less! Guaranteed.</p>
