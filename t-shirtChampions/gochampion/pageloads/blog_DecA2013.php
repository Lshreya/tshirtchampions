      <h2><strong>Together We Can: Part 2</strong></h2>
      <p>December 2013</p>
      <div>
<p>Suzie and I had just bought the balance of the company from my mom and brother in the Spring of 2001. We were, to the say the least, really happy, but really scared. We had a family meeting with our boys, then 15 and12. We told them things were gonna be tough for a while and we’d need their help. They really didn't want to work. But like all kids, they wanted stuff.Looking back, we couldn't have saved the company without them. Shortly after,we lost our biggest customer-25 % of the company.</p>
<p>Then, in the fall of that year, the course of American history was changed forever.  9/11as it has since become known, happened. Suzie and I had a kindred spirit withNew York. I had visited there since I was 18. I actually did quite a bit of business in the 80's on the same streets that were decimated. And my mom had just taken the boys to the Trade Towers Christmas of 2000!</p>
<p>As most people, I remember exactly what I was doing that morning. I was still at home and saw the second plane hit Tower II. My wife was at work and called that she was on her way to pick up our children from school.</p>
<p>As a family, we watched the days events unfold on TV. A surprising thing happened. The phone calls began at Champion and on our cell phones for flags, American Flags. The American Pride was swelling. We started calling vendors, but the flags were all gone.</p>
<p>That's when my sons said, "Dad, we gotta do something to help!" They suggested print the American Flag and sell them to raise money for the American Red Cross! That's when “Together We Can" was cemented into the heart of our little company.</p>
<p>We printed and sold over 5,000 tees and donated over $15,000 to the American Red Cross and other causes. We still have our "American Pride" and our"Together We Can" tees today.</p>
<p>So, when we decided to begin a new era in our company with the T-Shirt Champions brand, we needed a symbol. I told our Webmaster about our 9/11 experience. The Torch was born from our American Pride.</p>
<p>The Torch represents all the families, the teams, the schools, the non-profits, and the small businesses struggling to be Champions.So every time it gets tough, remember that "Together We Can" achieve greatness!</p>
