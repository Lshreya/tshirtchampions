<p class="darkertext"><strong>The Elections are not over!</strong><br />
  <br />
October 2012</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;   </p>
<div>
  <div>
      <p>      </p>
      <p> Well, that little election for the nation was completed a couple of  weeks ago. Thank goodness that one is over! If we spent as much time and  money (estimates of over 2 billion!) actually working on the problems  as we did on campaigning, we might get something done. But this  incessant politicking, when the Electoral College pretty much decides  the Presidential election back in October, is frustrating to me and  millions of others who wish to make an actual difference.</p>
      <p>   So now for some unabashed, shameless promotion. You can still make  a difference in the local political scene!! My friend Bill Kinkade (see  last blog &ldquo;Friends of a Brave Man&rdquo; on <a href="http://www.gochampion.net" _mce_href="http://www.gochampion.net">www.gochampion.net</a>) is in a runoff for State representative of District 52 in Marshall and Desoto counties. This election is not over!!!!</p>
      <p>    In the first election, the voters decided with 48% of the vote  that he was by far the most qualified. The antiquated law to perpetuate  elections says he should have at least 98 more votes, or 51%. Even with  his nearest competitor having less than 25% of the vote, he has to keep  politicking!</p>
      <p>It&rsquo;s true that all politics are local, and for those seeking to make a  difference with their vote, there is no better place to start than  their own backyard. Without the national election pushing people to the  polls, less than 50 voters could decide this local election. The  election will be more about the mobility of the electorate on Tuesday  11-27 than the candidates&rsquo; qualifications, much like the antiquated  Electoral College.</p>
      <p> GO VOTE for BILL KINKADE in District 52, if you live there!</p>
      <p>Call a voter that you know in Marshal or Desoto County and tell them to go exercise some real power and VOTE for Bill Kinkade!</p>
      <p>  Now for some Thanksgiving!</p>
      <p>&quot;I like being at the kids table at Thanksgiving. You can put your  elbows on it. You can feed the dog; you don't have to talk politics. No  matter how old I get, part of me remains at the kids table.&quot; -  Paraphrase John Hughes.</p>
      <p>Thanks to all of you who give thanks this time of year, for taking a  moment to reflect on your blessings. It is then we all realize that no  matter our situations, some poor soul is always in more need.</p>
      <p>Give someone a Hug and heartfelt Thanks!</p>
