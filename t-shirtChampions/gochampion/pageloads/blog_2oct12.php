<p class="darkertext"><strong>The Retirement Myth</strong><br />
  <br />
October 2012</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;   </p>
<div>
  <div>
      <p>      </p>
      <p _mce_style="padding-left: 30px;">&quot;The  last seven years have removed the imposters. It's now survival of the  quickest learners. If you are not continually learning and quickly  assimilating that information into your organization, you'll get left  behind and ultimately forgotten.&quot;  - Ken Edmundson</p>
      <p>     Ken is chairman of Edmundson Northstar Institute. On his website  he states: &quot;In Business you can have reason or results and reasons  don't count.&quot;</p>
      <p>     On the surface, Ken is a business coach. But he's also much more  than that. His teachings come from a man who has managed several  successful careers, written three books, and could have retired many  years ago! He refused. He has so much more to offer the world! He  certainly doesn't need the money. So when I saw his latest e-mail blast,  I distributed it to my team members as another business lesson. But  after some thought, I really think it is a life lesson. &quot;Do any of us  ever really want to be forgotten?&quot;  That would be worse than retirement  or dead. Statistics say that 20% of retirees pass away quicker because  they quit learning and aren't productive. It is in the human DNA to be  productive.</p>
      <p>     That's why I give you my Mom! Well, she's my Mom and you can't  have her. She &quot;retired&quot; in 1999 at the ripe old age of 59. Since then,  she built a new house; she went around the world with a new husband. She  became Chairman of Score, she teaches an entrepreneurial business class  at least once a month; she teaches in several prisons at least 6 times a  year, she still invests in the stock market regularly and she reads a  book every 2 weeks. Whew!</p>
      <p>     Mom’s life has not been without adversity. She has had two major  surgeries and actually went into coma from one of them. She recently  lost her husband after being his caregiver for several years. But she  refuses to &quot;retire.&quot;</p>
      <p>     In August Mom bought a 200-year-old house on the ocean in  Maine!!! Maine is a great place for us older folks who get to where we  can no longer stand the Memphis heat. Maine was also a romantic place  for her and her husband. I was lucky to spend almost three full weeks  with her in September. How many of us have spent three weeks at one time  on vacation, much less with your mom? I was the lucky one.  My Aunt  from Idaho, who is also refusing to retire, was there too.  I haven't  laughed so hard or become so relaxed in my adult life. Yes, we worked  hard on the grounds and the house to get it back into shape. They out  worked me everyday! But we had real fun, learning about Maine, learning  about our new neighbors, learning about each other.</p>
      <p>     So when you think of retiring, stop! The common saying is don't  retire just rewire!  Because if we quit learning, we will be forgotten!  My mom refuses to be forgotten! You should too!</p>
