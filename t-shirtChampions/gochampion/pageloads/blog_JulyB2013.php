      <p class="darkertext"><strong>The Stupid Tax</strong></p>
      <p>
      <br />July 2013</p>
      <div>
  <div>
<p>I have been writing these articles for three years now! This is my fifty-first article. Folks many times over have asked me, "why in the heck do you put yourself thru the discipline of writing a 500 word piece every 2 weeks?"
The simple answer is, I need the discipline!</p>
<p> There are really four other reasons why. First, I was an English Major in School, albeit the worst writer in most of my classes. Harry Rainey at Wooddale High School, along with my mom, instilled a passion for writing that I can't extinguish.
  Next, I have a fantastic family and a super team at Champion that I need to brag about!
  Third, I needed a method to ensure that my customers and contacts were accurate.
  And the fourth reason, I dearly wanted to save readers some "Stupid Tax".</p>
<p>The Stupid Tax, as defined by the Parkway Village dictionary of business terms, is the time, reputation, and money it costs us when we make stupid mistakes in business and in life.
  I will give you a couple of examples.</p>
<p>In 2002, I believed the mayors of the time. They walked hand in hand down Fourth Avenue near Beale Street. They proclaimed to the media and all present that the city was going to clean up and light the new entrance to Beale Street, and the Forum! I took it hook, line, and sinker. I went and opened a second gift shop on Beale near Fourth. I spent way too much money, and suffice to say it never made money and wasted a lot of my time. Moral: Don't be the first retailer in a new shopping area! Make sure it's a proven customer friendly venue. Stupid Tax paid!</p>
<p>Another Stupid thing I did just recently was the dog-poisoning incident. Wait now, I didn't kill anything except my pocketbook, and a whole Sunday!<br />
  Suzie and I live on four acres of swampland near the Wolf River, (another story.) We have 3 dogs and a pond, and I use very little pesticide, most years none at all. But this year, with no winter to speak of, the bugs are voracious. One Saturday I put out a bag of powder pesticide that was wet. It smelled like fermented ammonia, manure. I did not water it in the ground. In the hour before we brought him in for the evening, Jackson, our black-haired retriever, evidently took a bite of it! We did not know this at the time. The next morning he could not walk, and he was shaking all over. I threw a ninety-pound dog over my back and rushed to the emergency room. I had to go back home and retrieve the bag of pesticide so the docs could scrutinize the ingredients. Lots of time, (I removed all the chemical), lots of money, and 2 days later, Jackson was his same old self.</p>
<p>Moral of story: don't use pesticide in the incorrect manner! Even without dogs present. Stupid tax paid!
  
  I could write a whole book on my mistakes. But you know what they say:
  
  "A life spent making mistakes is not only more honorable but more useful than a life spent doing nothing."- George Bernard Shaw</p>
<p>The best thing now is I can forgive myself when I must pay the Stupid Tax! You can too! </p>
