<p class="darkertext"><strong>Reunion and </strong><strong>Redemption</strong><br />
  <br /> 
  February 2013
</p>
<div>
  <div>
    <p>I finally went to a small reunion of Lambuth College&nbsp;mates from the 70's. I say &ldquo;finally&rdquo; because I have been asked before. I&nbsp;always politely declined or allowed my schedule to conflict. I knew if I went, I would have to speak of the misdeeds of an angry teenager refusing to grow up. I will explain.</p>
    <p>In high school, I had been a reasonably successful student. I was a member of the Honor Society, President of Student Government,&nbsp;and Captain of the baseball team. I had my own car, lots of friends and, outwardly at least, I felt like an all around big&nbsp;man on the Wooddale campus. Deep down, I was a spoiled, angry, scared kid, hurtling headlong and unprepared toward adulthood and &ldquo;real-life&rdquo;.</p>
    <p>You see, despite appearances, I also was a drug addict. From the ages of 18 to 22, there wasn't a day I didn't drink, smoke, or ingest some kind of mind-altering substance. I spent 2 of those years at Lambuth College, and let&rsquo;s just say it wasn't pretty. Mind you, I had lots of &ldquo;fun&rdquo;. I became a fraternity man, of the Kappa Alpha Order, and I never let school get in the way of Sex, Drugs, and Rock n&rsquo; Roll. I used to joke that my&nbsp;priorities were to &ldquo;live hard, fast and get rich!&rdquo;</p>
    <p>There were just two things interfering with all that, my conscience, and the really nice, intelligent, Christian men and women&nbsp;of Lambuth College. They seemed to always be there to forgive and to try and help me thru these times. I had lost my moral compass, however and I really didn't want their help. Politely, I was asked to leave this small Methodist College. Instead of taking this opportunity to straighten up, I moved on to an even bigger party stage, Memphis State. Here I continued much of the same antics, and during that time I did some things that I have regretted for over 38 years.</p>
    <p>Later, in my forties, I began to see the folks from Lambuth again. I was polite, but the memories of all the bad things I did would creep back, and I was embarrassed when they remembered me! When reunions were discussed via Facebook I just disregarded them, assuming that they weren't for me. But this reunion was different in that my lifelong friend Dr. Tim Goldsmith and his wife were hosting. I&nbsp;have long admired Tim and looked up to him, but never showed it. His house was right down the road and somehow I new it was time for redemption.</p>
    <p>I've told my wife about those days, and I tried to explain that it was going to be hard. She reminded me how we&rsquo;ve all grown, and that she would be there to give her support. Well, redemption is mine! For three hours, I hugged, laughed, cried and asked forgiveness from more than one person.&nbsp;I still struggle to forgive myself for the time I wasted. But that Reunion of Redemption was a beginning!&nbsp;Thank you again Lambuth for being there!</p>
    <p>&quot;Seeking to forget makes exile all the longer; the secret of redemption lies in remembrance&quot;. ~ Richard Von Weizsaecker, German politician after WW2</p>
