<p class="darkertext"><strong>So God Made an Entrepreneur</strong><br />
  <br /> 
  February 2013
</p>
<div>
  <div>
    <p>I watch the Super Bowl mainly for the commercials. There! Now I finally admitted it! Sure, it's one the greatest sporting events of the year. But, I'm a homer. Unfortunately, we only have the Tennessee Titans in Nashville (not our home!) and I'm not really a fan. I do love the hype, the food, the camaraderie of friends seen once a year, and the friendly wager on the score by quarter.</p>

<p>What I really love are the creative and sometimes controversial commercials. Some are really lame and some are groundbreaking. Some are just to remind us how hard we work. Dodge Ram's "So God made a farmer" was one of those reminders.</p>

<p>Paul Harvey broadcast the original version in 1978, and after 35 years it still rings true. I always loved Paul Harvey; his plainspoken delivery was so laden with conviction. Whether you agreed with him or not, you wanted to! It was always clear that Paul Harvey was a true believer in the words he chose to share with us. This speech, brought back to us by a commercial of all things, has inspired me to write my own version. It's just a beginning. Nonetheless, I hope you will enjoy it. </p>

<p>So God made an entrepreneur</p>

<p>"I need a job creator. I need someone to come up with ideas for new businesses out of thin air and write them down in a plan.  I need somebody willing to keep her day job and risk sleep, family and wealth to begin a new business, with no guarantees. I need someone to get up before dawn and get the kids ready for school, work 8-10 hours at a job with no future and save enough money to risk it all on this idea."</p>

<p>So God made an entrepreneur</p>

<p>"I need someone with the intestinal fortitude to say to all the naysayers, "I can do this!"  And, "With your help, we can make our lives better." I need someone to risk his home, car, and sacrifice for a dream. Somebody to hire people with little or no experience, trust and train them to reach higher than they ever have before. Someone who can look beyond the inevitable stumbles and mistakes, someone who instead understands the value of learning and growing."</p>

<p>So God made an entrepreneur</p>

<p>God said, "I need someone to keep trying until they get it right, even if it means failing for a long time. Someone smart enough to deal with bankers one hour and strong enough to unload a truck the next. Someone who cares deeply about his customer, but even more about her employee, because they know that happy employees make happy customers. Someone willing to sacrifice profit for extra service when it "absolutely, positively must get done."</p>

<p>So God made an entrepreneur</p>

<p>" Someone who will keep their family together and silently rejoice when they and her Team have better lives because of a wild idea gone good"!</p>

<p>So God made an entrepreneur</p>
    
<p>   "Now you know the rest of the story." - Paul Harvey</p>
