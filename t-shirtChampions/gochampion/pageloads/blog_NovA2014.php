      <h2><strong>Leadership Made Simple, Part 3</strong><br /></h2>
      <p>November 2014</p>

      <div>
<p>Yes, this is the last in a series of Leadership Truths that I try to use each day. Sometimes they work and sometimes not. I do, however, know a couple of things about leadership that have always been true. </p>

<p>I cannot lead everyone the same way! I must tailor my style to my teammembers psychological profile.</p>

<p>In southern terms, "Ya gotta know the dogs in your yard." They all can't be trained the same way.</p>

<p>Also, it has taken me some 45 years to learn the most important thing. I can't make someone happy. </p>

<p>Happiness comes from deep within a person. How a person handles the little everyday issues that will pop up will be a reflection of their "happiness factor."  But that's the next lesson. </p>

<blockquote>"Leadership is lifting a person's vision to high sights, the raising of a person's performance to higher standard, the building of a personality beyond its normal limitations." 
<em style="display: block; float: right;">- Peter Drucker </em></blockquote>

<p><b>More Simple Leadership Truths</b></p>

<p>Seven. It's a number that is considered to be lucky, considered to be representative of wisdom and, according to a recent survey, the favorite number of more people than any other. It’s the perfect number to relate to the changes all leaders must face. </p>

<p>Leadership expert Ron Edmondson recently laid out his seven tips on how leaders can keep up in our fast-changing world.</p>

<p><b>1.</b> Read something different from what you normally read. As the pace of change accelerates, so does the interconnectivity of disciplines. To stretch, a leader should read something outside of his or her normal topics.</p>

<p><b>2.</b> Hang out with people who aren’t like you. It's comfortable to hang out with people like you, but it doesn't provide opportunities to learn new perspectives or appreciate diversity.</p>

<p><b>3.</b> Move forward on something with uncertainty. As Edmondson says, “The fact is, rarely will we have all the answers when making decisions.” Taking a risk on something new is a sure way to stretch yourself.</p>

<img src="http://www.tshirtchampions.com/sites/437/uploaded/images/14ChampionAwardsXmas.jpg" width="450" height="auto" alt="Call for our Winter Wearables specails!" style="margin: 0 auto;"/>
<br>
<br>

<p><b>4.</b> Attempt something you’ve never done. Along the same lines as taking risks, Edmondson suggests you do something beyond what you think you can do — be it taking a class, learning a new skill or attempting a home-repair project.</p>

<p><b>5.</b> Spend more time on opportunities than on problems. In most cases, opportunities energize us while problems weigh us down. Although we all have to handle some level of problems, it pays to focus more energy on advancement than repair.</p>

<p><b>6.</b> Schedule and discipline time to dream. Take a walk. Read. Meditate. Make time to dream. As we get caught up in operations, it can be hard to find the time to just think and dream. Make the time.</p>

<p><b>7.</b> Stay physically active. Numerous studies indicate what experience already tells most of us: When you stretch your body you stretch your mind.</p>

<p><i>Source: Ron Edmondson has more than 30 years of leadership experience, mostly as a self-employed business owner, and has been in full-time ministry for just over a decade. He has successfully led the restart of one church and the planting of two churches. He has a seminary master’s and a master’s in organizational leadership.</i></p>


<blockquote>"Leaders need to be optimists. Their vision is beyond the present." 
<em style="display: block; float: right;">- Rudy Giuliani</em></blockquote>


