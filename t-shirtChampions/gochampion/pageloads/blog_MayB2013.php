      <p class="darkertext"><strong>Memphis Rocks II</strong></p>
      <p>
      <br />May 2013</p>
      <div>
  <div>
<p>In September last year, I wrote how the theme "Memphis Rocks "was born. Jeremy Parks of the Lipscomb and Pitts Breakfast Club and I kicked around ideas for one really positive slogan that Memphis can tell the world.  Since then, we have finally reached the tipping point.</p>
    <blockquote>"The tipping point is that magic moment when an idea, trend or social behavior crosses a threshold, tips and spreads like wildfire. "  ~ Malcolm Gladwell, Author of "The Tipping Point", and many other wonderful social trends, studies and books.</blockquote>
    <p>Champion designed and produced a cool logo and t-shirt. We have sold enough to donate $6000 to the Fallen Officer Memorial. The LPBC club and members have contributed much more. We are the only major metropolitan city in USA without such a monument. It's going to cost over 1 million, and every little bit helps. See below for new Memphis Rocks tees and how you can purchase and contribute! The Convention and Business Bureau Tourist centers and Beale Street Gift Shop are on board selling the tees too. Your company can even purchase and Champion will place your place their logo on the back if you wish!</p>
    <p>So many other great things have been born out of the simple idea that Memphis Rocks. The LPBC is a catalyst for screaming this message to not only the rest of the country but mainly to us. Memphians are a fickle bunch; we are not only way too hard on each other but our city as well.</p>
    <blockquote>"You can't hold a man down without staying down there with him " Booker T Washington, author, educator.</blockquote>
    <p>We have too long held each other down for the bad things in the past that plagued this city. Social scientists have stated it can take several generations to remove the stigma and effects of oppression. Memphians have a tendency to repeat a lot of problems without offering solutions. We can do better than that, because, well Memphis Rocks!</p>
    <p>I'm writing this after seeing our very own Grizz go up 3-1 to win the NBA Western Conference Finals.  I'm stating now, we will win this series because Memphis Rocks! </p>
<p>LPBC and Clear Channel Outdoor has placed billboards all over town. The Commercial Appeal has run ads to promote the sale of Memphis Rocks! Tees to help build the Fallen Officer Memorial. Now the CA is going to print the messages from the billboards. LPBC produced commercials with the mayors and other dignitaries, screaming that Memphis Rocks. <a href="http://www.thesparktv.org/">"The Spark" TV show</a>, produced by Jeremy, Media Source, and WKNO now airs every other Thursday at 9:00 pm. This wonderful, 30-minute show is truly showing how Memphis Rocks! I will be one of the guests this Thursday speaking on Champion's " Late Bloomers " program of hiring ex-offenders for the last 5 years. It's working. Tune in to see how!</p>
<p>The <a href="http://onlocationmemphis.org/">On Location: Memphis International Film & Music Festival</a> featured several films commissioned by LPBC, and produced by some of its members. Click the link above to see more reasons why Memphis Rocks!</p>
<p>In closing, I would like you to help me see a change in our collective theory towards our beloved town. I'd like to see this happen before my time comes to an end. We have a few years, I hope. But I am a very impatient soul. We can do anything together as long as we treat each other as equals or better! Because Memphis Rocks!</p>
