<p>  Like many small businesses, we have built and rebuilt our website at <a href="http://www.gochampion.net/" _mce_href="http://www.gochampion.net/">www.gochampion.net</a> more times than I care to remember. Because, as we learned, you really  never quit building a website. We spent many hours updating pictures and  tweaking our content because, like many of you, we knew that a business  today must have a website, but that&rsquo;s about it. Champion Awards &amp;  Apparel was online, but our &ldquo;business&rdquo; was not.</p>
<p>     Three years ago I set my team and myself the task of making our  website into something more useful than a repository for phone numbers,  or a map to our showroom. I had grand ideas about transforming our  business, using technology to update our 45-year-old business model for  the new millennium. In a nutshell, I thought we could build the best  online t-shirt design site in the marketplace. Oh, how naive I was! We  have had a person performing Webmaster duties since 1996. Not  necessarily the same one, (first mistake), but we have remained up to  date in our research. We wrote a cursory business plan with a pretty  small budget, (second mistake). We then set out to hire the best company  we could afford that could help us program a site.</p>
<p>     Now, when I say &ldquo;program&rdquo;, I can only tell you that our idea  would require something custom, above and beyond what your average web  designer could accomplish. There is some great talent available in  Memphis, but we needed a broader pool from which to draw the very  specific skills our project required. So we looked outside our hometown  as well, this turned out to be the third mistake.  This particular  mistake could warrant an entire blog of its own, but I will just say  here that we received exactly what we asked for, with our champagne  taste and beer budget! After a year of wasted time and money we came  back around full circle. Note to Self: Always demand a schedule and  strict deadline for performance with service companies. Web providers  are no different!</p>
<p>     As we learned more about the nature of what we were attempting,  and refined our original vision into something more realistic, we  brought our search back to Memphis. We vetted 5 more companies that were  all good in their own areas of expertise, and we learned that not all  web companies could do the same things. Our search finally paid off when  we found Rocket Fuel, a Memphis web company that, like ours, is family  owned and operated, and just plain good at what they do! We hired Rocket  Fuel to help us launch <a href="http://tshirtchampions.com/" _mce_href="http://tshirtchampions.com/">tshirtchampions.com</a> by December of this year. The Champion team has really come together  and is working daily with Rocket Fuel. Now finally, with God willing,  and &ldquo;if the creek don't rise&rdquo;, our goal is in sight! It has been a  three-year undertaking and we will never be finished. But we are  excited, and we can&rsquo;t wait to show you how <a href="http://Tshirtchampions.com/" _mce_href="http://Tshirtchampions.com/">Tshirtchampions.com</a> promises to be the easiest and least expensive way to design t-shirts online.</p>
