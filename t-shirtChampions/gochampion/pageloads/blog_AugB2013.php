<p>     This will be the last of my articles on Stupid mistakes. I hope  you&rsquo;ll see from my own examples that &ldquo;Stupid Taxes&quot; are just as  inevitable as the other kind. That is, if you&rsquo;re not making mistakes,  then you&rsquo;re not really trying! &ldquo;Stupid Tax&rdquo; is just a funny phrase I use  to keep myself humble. In fact, mistakes are a necessary part of  learning, and the only truly  &ldquo;Stupid&rdquo; tax is the one you pay for giving  up! And, Dear Readers, if you   only get one message from these stories  of mine, I hope it&rsquo;s this;   &ldquo;Never give up!&rdquo;</p>
