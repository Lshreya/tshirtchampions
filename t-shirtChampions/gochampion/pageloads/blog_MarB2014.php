          <h2><strong>A Success Story Every 2 Weeks</strong><br /></h2>
      <p>March 2014</p>
      
      <div>
		<p>Over the years, my family and small company have received some great recognition. Memphis loves a success story and Champion is just that. We are successful in the "American Way." And we love what we do everyday! We've been in business for over 44 years. We have over 40 employees who have nearly 70 children to support. That being said, we sincerely thank you for the business we receive.</p>

<p>But…there is a "but."  Seems like there's always a but, isn't there? Success is an intangible, fleeting thing.</p>

<p>The saying, "The harder you work the luckier you get" applies to most small family businesses. Champion is no different! However, t-shirts and trophies aren't one of the greatest money making products. The truth is that they're commodities where the order normally goes to the lowest bidders. So, when I say fleeting I mean all the press and accolades don't outweigh an organizations budget. I still get calls every week asking me, "Is this order too small?" Or, "Will you have time to do this order? I know you guys are so busy."  The best one is "Wow, y'all are so successful, Champion doesn't need our business." </p>

<p>Now, read this next statement very slowly. Myself, Champion, our families and their children not only want, but MUST have your business. </p>

<p>We will beat all published quoted prices. Because we have such a large family for the amount of t-shirt and trophy business in Memphis, we will take just about any order at any price. That's a promise. </p>

<p>Why? I AM ONLY SUCCESSFUL every 2 weeks. That is when I ask my wife (Champion's CFO) if we can meet payroll. And you know what? We have been successful every 2 weeks for 44 years – with your help!</p>

<p>This is why I am asking you to please spread the word, we want and need your business. Our family and success depends on it! </p>

<p><blockquote>"Success is not the key to happiness. Happiness is the key to success. If you love what you are doing, you will be successful."  <br><span style="float:right">– Albert Schweitzer</span><br></blockquote></p>
