               <blockquote>
                 <p>
                        &quot;I have come to the frightening conclusion that I  am the 'Decisive Element'. It is my personal approach that creates the  climate. It is my daily mood that makes the weather.<br />
                   <br />
                        I possess tremendous power to make life miserable or joyous.  I can be a tool of torture or an instrument of inspiration.<br />
                   <br />
                        I can humiliate or humor, hurt or heal.... I am the 'Decisive Element'.<br />
                   <br />
                         In all situations, it is my response that decides whether a crisis is  escalated or de-escalated, and a person humanized or de-humanized.<br />
                   <br />
                         If I treat people as they are, I make them worse.  If I treat people as  they ought to be, I help them become what they are capable of becoming  ... I am the 'Decisive Element'.&quot;</p>
