 football and basketball games. Champion enters its 9th year and 4th year respectively as the exclusive licensee for the AutoZone Liberty Bowl and Godaddy.com Bowl. Champion also designs, produces, and merchandises virtually all AutoZone Liberty Bowl and Godaddy.com Bowl merchandise. Champion Awards is also a 25-year member of SGIA, ASI and PPAI and 40 year member of the Awards Recognition Association and the Memphis Chamber of Commerce. <!--
